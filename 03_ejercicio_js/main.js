$("#elem1").on("mouseover", function(){
    $("img").attr("src" , "img/arbol.jpg");
    $("#elem1").addClass("destacar");
 console.log("Algo ha ocurrido");
});

$("#elem1").on("mouseout", function(){
    $("img").attr("src" , "img/blanco.jpg");
    $("#elem1").removeClass("destacar");
 console.log("Algo ha ocurrido");
});



